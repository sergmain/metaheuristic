package ai.metaheuristic.ai.dispatcher.thread;

import lombok.extern.slf4j.Slf4j;

import java.lang.management.ManagementFactory;
import java.lang.management.ThreadInfo;
import java.lang.management.ThreadMXBean;

/**
 * @author Serge
 * Date: 11/27/2020
 * Time: 2:02 AM
 */
@Slf4j
public class DeadLockDetector {

    public static void findDeadLocks() {
        ThreadMXBean tmx = ManagementFactory.getThreadMXBean();
        long[] ids = tmx.findDeadlockedThreads();
        if (ids != null) {
            ThreadInfo[] infos = tmx.getThreadInfo(ids, true, true);
            log.error("!!! Following Threads are deadlocked, total: {}", infos.length);
            int idx=1;
            for (ThreadInfo info : infos) {
                log.error("!!! {}/{} {}", idx++, infos.length, info);
            }
        }
/*
        else {
            log.info("There isn't any dead lock");
        }
*/
    }
}
