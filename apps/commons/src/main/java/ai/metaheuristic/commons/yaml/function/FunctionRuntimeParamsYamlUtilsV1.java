package ai.metaheuristic.commons.yaml.function;

import ai.metaheuristic.commons.S;
import ai.metaheuristic.commons.exceptions.BlankYamlParamsException;
import ai.metaheuristic.commons.yaml.YamlUtils;
import ai.metaheuristic.commons.yaml.versioning.AbstractParamsYamlUtils;
import org.springframework.lang.NonNull;
import org.yaml.snakeyaml.Yaml;

/**
 * @author Serge
 * Date: 6/17/2019
 * Time: 12:10 AM
 */
public class FunctionRuntimeParamsYamlUtilsV1
        extends AbstractParamsYamlUtils<FunctionRuntimeParamsYamlV1, FunctionRuntimeParamsYaml, Void, Void, Void, Void> {

    @Override
    public int getVersion() {
        return 1;
    }

    @NonNull
    @Override
    public Yaml getYaml() {
        return YamlUtils.init(FunctionRuntimeParamsYamlV1.class);
    }

    @NonNull
    @Override
    public FunctionRuntimeParamsYaml upgradeTo(@NonNull FunctionRuntimeParamsYamlV1 src) {
        src.checkIntegrity();
        FunctionRuntimeParamsYaml trg = new FunctionRuntimeParamsYaml();
        trg.states.putAll(src.states);
        trg.checkIntegrity();
        return trg;
    }

    @NonNull
    @Override
    public Void downgradeTo(@NonNull Void yaml) {
        return null;
    }

    @Override
    public Void nextUtil() {
        return null;
    }

    @Override
    public Void prevUtil() {
        return null;
    }

    @Override
    public String toString(@NonNull FunctionRuntimeParamsYamlV1 yaml) {
        return getYaml().dump(yaml);
    }

    @NonNull
    @Override
    public FunctionRuntimeParamsYamlV1 to(@NonNull String yaml) {
        if (S.b(yaml)) {
            throw new BlankYamlParamsException("'yaml' parameter is blank");
        }
        final FunctionRuntimeParamsYamlV1 p = getYaml().load(yaml);
        return p;
    }

}
